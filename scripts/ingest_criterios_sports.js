// Main
// ═══════════════════════════════════════════════════════════════

async function main() {
  const raw = fs.readFileSync(RAW_TXT, 'utf8');
  const blocks = parseRawText(raw);
  console.log(`Parsed ${blocks.length} question blocks`);

  const conn = await mysql.createConnection({
    host: process.env.DB_HOST || 'localhost',
    port: parseInt(process.env.DB_PORT || '3306', 10),
    user: process.env.DB_USER || 'root',
    password: process.env.DB_PASS || '',
    database: process.env.DB_NAME || 'eplus_tools',
    charset: 'utf8mb4'
  });

  // Find program
  const [progs] = await conn.query(
    `SELECT id, name FROM intake_programs WHERE name LIKE ?`,
    [PROGRAM_NAME_LIKE]
  );
  if (!progs.length) { console.error('Program not found'); process.exit(1); }
  const programId = progs[0].id;
  console.log(`Program: ${progs[0].name} (${programId})`);

  // Fetch existing questions for this program
  const [questions] = await conn.query(
    `SELECT eq.id, eq.code, eq.title
     FROM eval_questions eq
     JOIN eval_sections es ON eq.section_id = es.id
     WHERE es.program_id = ?`,
    [programId]
  );
  const qByCode = {};
  questions.forEach(q => { qByCode[q.code] = q; });

  let updated = 0, inserted = 0, deleted = 0;

  for (const block of blocks) {
    const q = qByCode[block.code];
    if (!q) {
      console.warn(`  ⚠ question ${block.code} not in DB (skipping)`);
      continue;
    }

    // ── Update Part A + description ──────────────────
    const partA = PART_A[block.code] || {};
    let description = null;
    if (block.code === '4.1') {
      // 4.1 body is empty (all content in wps[]), use title as description
      description = 'Provide a brief description of the overall structure of the work plan (list of work packages or graphical presentation).';
    } else {
      const crit = extractCriterion(block.body);
      description = crit.description || null;
    }

    await conn.query(
      `UPDATE eval_questions
       SET description = COALESCE(?, description),
           general_context = ?,
           connects_from = ?,
           connects_to = ?,
           global_rule = ?
       WHERE id = ?`,
      [description, partA.general_context || null, partA.connects_from || null,
       partA.connects_to || null, partA.global_rule || null, q.id]
    );
    updated++;

    // ── Delete existing criteria for this question ──────
    const [existing] = await conn.query(
      `SELECT COUNT(*) as n FROM eval_criteria WHERE question_id = ?`, [q.id]
    );
    if (existing[0].n > 0) {
      await conn.query(`DELETE FROM eval_criteria WHERE question_id = ?`, [q.id]);
      deleted += existing[0].n;
    }

    // ── Insert new criterion/criteria ──────────────────
    if (block.code === '4.1') {
      // 5 criteria (WP1..WP5)
      for (let wi = 0; wi < block.wps.length; wi++) {
        const wp = block.wps[wi];
        const crit = extractCriterion(wp.body);
        const id = crypto.randomUUID();
        await conn.query(
          `INSERT INTO eval_criteria (id, question_id, title, max_score, mandatory, priority,
           intent, elements, example_weak, example_strong, avoid, sort_order)
           VALUES (?,?,?,?,?,?,?,?,?,?,?,?)`,
          [id, q.id,
           (crit.tituloCorto || `WP${wp.wpNum}`),
           1, crit.mandatory ? 1 : 0, 'alta',
           crit.intent || null, crit.elements || null,
           crit.example_weak || null, crit.example_strong || null,
           crit.avoid || null, wi]
        );
        inserted++;
      }
    } else {
      const crit = extractCriterion(block.body);
      const id = crypto.randomUUID();
      await conn.query(
        `INSERT INTO eval_criteria (id, question_id, title, max_score, mandatory, priority,
         intent, elements, example_weak, example_strong, avoid, sort_order)
         VALUES (?,?,?,?,?,?,?,?,?,?,?,?)`,
        [id, q.id,
         crit.tituloCorto || q.title,
         1, crit.mandatory ? 1 : 0, 'alta',
         crit.intent || null, crit.elements || null,
         crit.example_weak || null, crit.example_strong || null,
         crit.avoid || null, 0]
      );
      inserted++;
    }
    console.log(`  ✓ ${block.code} ${q.title.substring(0, 50)}`);
  }

  console.log(`\n─── Done ──────────────────────────────`);
  console.log(`Questions updated:   ${updated}`);
  console.log(`Old criteria deleted: ${deleted}`);
  console.log(`New criteria inserted: ${inserted}`);

  await conn.end();
}

main().catch(e => { console.error(e); process.exit(1); });
