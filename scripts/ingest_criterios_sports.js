// Parser: extract question blocks from raw text
// ═══════════════════════════════════════════════════════════════

function parseRawText(text) {
  const lines = text.split('\n');
  // Find question headers: "1.1 ...", "2.1.1 ...", etc., excluding section headers "1. RELEVANCE", "2. QUALITY"
  const qRegex = /^(\d+\.\d+(?:\.\d+)?)\s+(.+?)\s*$/;
  const sectionRegex = /^\d+\.\s+[A-Z][A-Z\s]+$/;
  const wpRegex = /^Work package (\d+)\s*$/;

  const blocks = [];
  let current = null;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    if (sectionRegex.test(line)) continue; // skip "1. RELEVANCE", etc.
    if (line === 'PARTE 1' || line === 'PARTE 2' || line === 'CRITERIOS') continue;
    if (line === '=== TABLES ===' || line.startsWith('--- Table')) break;

    const m = line.match(qRegex);
    if (m) {
      if (current) blocks.push(current);
      current = { code: m[1], title: m[2], body: [], wps: [] };
      continue;
    }

    const wpm = line.match(wpRegex);
    if (wpm && current && current.code === '4.1') {
      current.wps.push({ wpNum: parseInt(wpm[1], 10), body: [] });
      continue;
    }

    if (current) {
      if (current.code === '4.1' && current.wps.length > 0) {
        current.wps[current.wps.length - 1].body.push(line);
      } else {
        current.body.push(line);
      }
    }
  }
  if (current) blocks.push(current);
  return blocks;
}

// Split body into criterion-like structure
function extractCriterion(lines) {
  // find indexes of headers: "Título corto", "INTENCIÓN", "ELEMENTOS", "EJEMPLOS", "EVITAR"
  const result = { description: '', tituloCorto: '', mandatory: false, intent: '', elements: '', example_weak: '', example_strong: '', avoid: '' };
  const sections = { desc: [], tit: [], intent: [], elements: [], ejemplos: [], avoid: [] };
  let current = 'desc';

  let sawTituloCorto = false;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (/^T[íi]tulo corto/i.test(line)) {
      current = 'tit';
      sections.tit.push(line);
      sawTituloCorto = true;
      continue;
    }
    // "| Category | Obligatorio" marker: only pull-back-from-desc if we haven't
    // already captured an explicit "Título corto:" line.
    if (/^\|\s*[\w\/]+\s*\|\s*Obligatorio/i.test(line)) {
      if (!sawTituloCorto) {
        for (let j = sections.desc.length - 1; j >= 0; j--) {
          const prev = sections.desc[j].trim();
          if (prev) {
            sections.tit.push(prev);
            sections.desc.splice(j, 1);
            break;
          }
        }
      }
      sections.tit.push(line);
      current = 'tit';
      continue;
    }
    if (/^INTENCIÓN$/i.test(line) || /^INTENCION$/i.test(line)) { current = 'intent'; continue; }
    if (/^ELEMENTOS$/i.test(line)) { current = 'elements'; continue; }
    if (/^EJEMPLOS/i.test(line)) { current = 'ejemplos'; continue; }
    if (/^EVITAR$/i.test(line)) { current = 'avoid'; continue; }
    sections[current].push(line);
  }

  result.description = sections.desc.join(' ').trim();

  // Title line(s) — extract short title + detect "Obligatorio"
  // Try "Título corto: ..." form first; otherwise fall back to the preceding
  // line captured right before "| Category | Obligatorio".
  const titJoined = sections.tit.join(' ');
  const titMatch = titJoined.match(/Título corto:\s*([^|]+?)(?:\s*\||$)/i);
  if (titMatch) {
    result.tituloCorto = titMatch[1].trim();
  } else {
    // Look for a line that doesn't contain "|Obligatorio" — it's the pre-marker title
    for (const l of sections.tit) {
      if (!/^\|/.test(l) && !/Título corto/i.test(l)) {
        result.tituloCorto = l.trim();
        break;
      }
    }
  }
  result.mandatory = /Obligatorio/i.test(titJoined);

  // Split ejemplos into weak/strong
  const ejem = sections.ejemplos.join(' ').trim();
  const weakIdx = ejem.search(/A\s+(?:weak|poor)\s+(?:example|risk analysis|structure)/i);
  const strongIdx = ejem.search(/A\s+strong\s+(?:example|risk analysis|structure)/i);
  if (weakIdx >= 0 && strongIdx > weakIdx) {
    result.example_weak = ejem.substring(weakIdx, strongIdx).trim();
    result.example_strong = ejem.substring(strongIdx).trim();
  } else if (strongIdx >= 0) {
    result.example_weak = ejem.substring(0, strongIdx).trim();
    result.example_strong = ejem.substring(strongIdx).trim();
  } else {
    // fallback: split by first sentence if "weak"/"strong" tokens missing
    result.example_weak = ejem;
    result.example_strong = '';
  }

  result.intent = sections.intent.join(' ').trim();
  result.elements = sections.elements.join(' ').trim();
  result.avoid = sections.avoid.join(' ').trim();
  return result;
}

